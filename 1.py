#Тимофей составляет 5-буквенные коды из букв Т, И, М, О, Ф, Е, Й. 
# Буква Й может использоваться в коде не более одного раза, 
# при этом она не может стоять на первом месте, на последнем месте и рядом с буквой И. 
# Все остальные буквы могут встречаться произвольное количество раз или не встречаться совсем. 
# Сколько различных кодов может составить Тимофей?


import itertools
alphabet = "ТИМОФЕЙ"
count = 0

for e in itertools.product(alphabet, repeat=5):
    # Проверки, не зависящие от позиции (делаем один раз)
    if e.count('Й') > 1 or e[0] == 'Й' or e[-1] == 'Й':
        continue
        
    # Проверка на соседство с И
    bad_neighbor = False
    for i in range(len(e) - 1):
        if (e[i] == 'Й' and e[i+1] == 'И') or (e[i] == 'И' and e[i+1] == 'Й'):
            bad_neighbor = True
            break
            
    if not bad_neighbor:
        count += 1

print(count)