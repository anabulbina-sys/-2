#Сколько единиц содержится в двоичной записи значения выражения: 4^2020 + 2^2017 − 15?
# Вычисляем значение выражения
# 4^2020 = (2^2)^2020 = 2^(2*2020) = 2^4040
# 2^2017 = 2^2017
# 15 = 1111 в двоичной системе

result = 2**4040 + 2**2017 - 15

# Переводим в двоичную строку (без префикса '0b')
binary = bin(result)[2:]

# Считаем количество единиц
count_ones = binary.count('1')

print(f"Двоичная запись (первые 50 символов): {binary[:50]}...")
print(f"Количество единиц: {count_ones}")